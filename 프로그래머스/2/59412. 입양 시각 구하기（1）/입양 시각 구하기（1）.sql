SELECT 
    CASE 
        WHEN TIME(DATETIME) >= '09:00:00' AND TIME(DATETIME) < '10:00:00' THEN '9'
        WHEN TIME(DATETIME) >= '10:00:00' AND TIME(DATETIME) < '11:00:00' THEN '10'
        WHEN TIME(DATETIME) >= '11:00:00' AND TIME(DATETIME) < '12:00:00' THEN '11'
        WHEN TIME(DATETIME) >= '12:00:00' AND TIME(DATETIME) < '13:00:00' THEN '12'
        WHEN TIME(DATETIME) >= '13:00:00' AND TIME(DATETIME) < '14:00:00' THEN '13'
        WHEN TIME(DATETIME) >= '14:00:00' AND TIME(DATETIME) < '15:00:00' THEN '14'
        WHEN TIME(DATETIME) >= '15:00:00' AND TIME(DATETIME) < '16:00:00' THEN '15'
        WHEN TIME(DATETIME) >= '16:00:00' AND TIME(DATETIME) < '17:00:00' THEN '16'
        WHEN TIME(DATETIME) >= '17:00:00' AND TIME(DATETIME) < '18:00:00' THEN '17'
        WHEN TIME(DATETIME) >= '18:00:00' AND TIME(DATETIME) < '19:00:00' THEN '18'
        WHEN TIME(DATETIME) >= '19:00:00' AND TIME(DATETIME) < '20:00:00' THEN '19'
        ELSE NULL
    END AS HOUR,
    COUNT(ANIMAL_ID) AS COUNT
FROM ANIMAL_OUTS
GROUP BY HOUR
HAVING HOUR IS NOT NULL
ORDER BY TIME(DATETIME)
